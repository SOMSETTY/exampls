package Day3;

public class swapingoftwovarable {

	public static void main(String[] args) {
		 
		        int a = 5, b = 10;
		        System.out.println("Before swap: a = " + a + ", b = " + b);

		        int temp = a; // store a
		        a = b;        // assign b to a
		        b = temp;     // assign temp to b

		        System.out.println("After swap: a = " + a + ", b = " + b);
		    
		}

	}


